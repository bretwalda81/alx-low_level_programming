#include "3-calc.h"
#include <stdio.h>

/**
* op_add - add a and b
* @a: first number
* @b: econd number
* Return: answer
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub - difference of a and b
* @a: first number
* @b: econd number
* Return: answer
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul - multiples a and b
* @a: first number
* @b: econd number
* Return: answer
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_div - division of a by b
* @a: first number
* @b: econd number
* Return: answer
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod - remainder of the division of a by b
* @a: first number
* @b: econd number
* Return: answer
*/
int op_mod(int a, int b)
{
return (a % b);
}
